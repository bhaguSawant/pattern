#Checking whether a Character is Alphabet or Not in Python ?

# Python Program to find character is alphabet or not

# user input
ch = 'z'

# basic logic
if 'a' <= ch <= 'z' or 'A' <= ch <= 'Z':
    print("The  character", ch, "is an Alphabet")
else:
    print("The  character", ch, "is not an Alphabet")